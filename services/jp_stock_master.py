def get_all_jp_tickers() -> list[str]:
    return list(JP_STOCK_MASTER.keys())


def get_jp_name(ticker: str) -> str:
    return JP_STOCK_MASTER.get(ticker, ticker)


def search_jp_master(query: str) -> list[dict]:
    """日本語名・コードでファジー検索し、スコア順で返す"""
    from difflib import SequenceMatcher

    q = query.strip()
    if not q:
        return []
    q_lower = q.lower()

    scored = []
    for ticker, name in JP_STOCK_MASTER.items():
        code = ticker.replace(".T", "").lower()
        n_lower = name.lower()

        score = 0
        if q_lower in (code, ticker.lower(), n_lower):
            score = 100
        elif code.startswith(q_lower) or n_lower.startswith(q_lower):
            score = 90
        elif q_lower in code or q_lower in n_lower:
            score = 80
        elif all(c in n_lower for c in q_lower):
            score = 65
        else:
            ratio = max(
                SequenceMatcher(None, q_lower, n_lower).ratio(),
                SequenceMatcher(None, q_lower, code).ratio(),
            )
            if ratio >= 0.40:
                score = int(ratio * 60)

        if score > 0:
            scored.append({
                "symbol": ticker,
                "name": name,
                "exchange": "東証",
                "score": score,
            })

    scored.sort(key=lambda x: x["score"], reverse=True)
    return scored
